package com.corp.oa.system.system;

import java.util.ArrayList;
import java.util.Collection;

import com.corp.frame.action.FrameForm;
import com.corp.frame.core.dao.DataObject;

public class SysForm extends FrameForm {

	/**
	 * 
	 */
	private static final long serialVersionUID = -8278776634185567236L;

	private Collection<DataObject> infolist = new ArrayList<DataObject>();

	private String[] names;
	private String[] attrs;

	public Collection<DataObject> getInfolist() {
		return infolist;
	}

	public void setInfolist(Collection<DataObject> infolist) {
		this.infolist = infolist;
	}

	public String[] getAttrs() {
		return attrs;
	}

	public void setAttrs(String[] attrs) {
		this.attrs = attrs;
	}

	public String[] getNames() {
		return names;
	}

	public void setNames(String[] names) {
		this.names = names;
	}
}
